<template><div><h1 id="overview" tabindex="-1"><a class="header-anchor" href="#overview" aria-hidden="true">#</a> Overview</h1>
<p>This extends the <a href="https://v2.vuepress.vuejs.org/reference/default-theme/config.html#basic-config" target="_blank" rel="noopener noreferrer">default VuePress 2 theme<ExternalLinkIcon/></a> with some extra power and features such as:</p>
<ul>
<li>Easily distributable default config sets and shared navbar items</li>
<li>Additional <code v-pre>containers</code> to help spruce up your Markdown</li>
<li>Google Analytics/Tag Manager support</li>
<li>HubSpot tracking code support</li>
<li>Automatic metatag generation</li>
<li>Features to help combining different docs sites into a single coherent docs experience</li>
<li><code v-pre>Docsearch</code> support</li>
<li>Customizable <code v-pre>SidebarHeader</code> header for larger and more complex sites</li>
<li>Automatically generated and customizable <em>Previous Versions</em> page</li>
<li>Automatically generated and customizable <em>Contributorz</em> page</li>
<li>Additional <code v-pre>Guide</code> page type</li>
<li>Additional global components for <code v-pre>YouTube</code>,  <code v-pre>MailChimp</code>, <code v-pre>Jobs</code> and <code v-pre>Sponsors</code></li>
<li><code v-pre>robots.txt</code> and <code v-pre>sitemap.xml</code> generation</li>
<li>Customizable <code v-pre>columns</code> for <code v-pre>Navbar</code> subitems</li>
<li><a href="https://www.carbonads.net/" target="_blank" rel="noopener noreferrer">CarbonAds<ExternalLinkIcon/></a> support</li>
</ul>
